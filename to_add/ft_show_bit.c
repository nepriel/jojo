#include "libft.h"

void	show_bit(int nb)
{
	int i;

	i = (sizeof(char) * 8) - 1;
	while (i >= 0)
	{
		if (nb & (1u << i))
			write(1, "1", 1);
		else
			write(1, "0", 1);
		i--;
	}
}
